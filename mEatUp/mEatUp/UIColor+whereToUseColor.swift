import UIKit

extension UIColor {
    
    class func navigationBarBarTintColor() -> UIColor {
        return UIColor.redOrangeColor()
    }
    
    class func navigationBarTintColor() -> UIColor {
        return UIColor.whiteColor()
    }
    
    class func navigationBarTitleTextColor() -> UIColor {
        return UIColor.whiteColor()
    }
    
    class func searchBarTintColor() -> UIColor {
        return UIColor.redOrangeColor()
    }
    
    class func tabBarBarTintColor() -> UIColor {
        return UIColor.redOrangeColor()
    }
    
    class func tabBarTintColor() -> UIColor {
        return whiteColor()
    }
    
    class func loginButtonBackgroundColor() -> UIColor {
        return whiteColor()
    }
    
    class func loginButtonBorderColor() -> UIColor {
        return UIColor.redOrangeColor()
    }
    
    class func loginButtonTitleColorNormal() -> UIColor {
        return UIColor.redOrangeColor()
    }
    
    class func loginButtonTitleColorHighlited() -> UIColor {
        return UIColor.aluminiumColor()
    }
}
